function falaOi() {
    console.log('Oi');
}
falaOi();

const souUmDado = function() {
    console.log('Sou um dado.');
}
souUmDado();

const funcaoArrow = () => {
    console.log('Sou uma arrow function');
}
funcaoArrow();

const obj = {
    falar() {
        console.log('Sou uma função dentro de um objeto.');
    }
}
obj.falar();